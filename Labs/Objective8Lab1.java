public class Objective8Lab1 {
  public static void main(String[] args) {
    int count = 1;
    int counter = 10;
    while(count < 11) {
      System.out.println(count);
      count = count + 1;
    }
    while(counter > 0) {
      System.out.println(counter);
      counter = counter - 1;
    }
  }
}
